        <div class="panel-heading" align="center"><b> Présentation de l'IRP-INFO</b></div>
        <div class="panel-body">

        	<p align="justify">

L'Inspection Régionale de Pédagogie (IRP) est un service de la Délégation Régionale des Enseignements Sécondaires (DRES), qui s'occupe de l'enseignement des disciplines dont elle a la charge.<br/>
L'IRP-INFO de la DRES-EN a plusieurs missions :<br/>
<ul>
	<li>Formation, encadrement et suivi des enseignants d'informatique de la région;</li>
	<li>Formation, encadrement, suivi et évaluation des élèves-professeurs de l'ENS de Maroua;</li>
	<li>Suivi des CRM et salle spécialisées d'informatique de la réunion;</li>
	<li>Etc.</li>
</ul>



